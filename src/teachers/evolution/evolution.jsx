import React, { useEffect, useState, useMemo } from "react";
import Sidebar from "../../components/sidebarAdmin";
import SearchAndSort from "../../components/search";
import VideoChart from "./charts/VideoChart";
import PrePostChart from "./charts/PrePostChart";
import QuizChart from "./charts/QuizChart";
import { getAllClassrooms, getStudentSummary } from "../../api/teachers/evolution";
import { getSubchapters } from "../../api/teachers/subchapter";

export default function ProgressDashboard() {
    const [students, setStudents] = useState([]);
    const [classrooms, setClassrooms] = useState([]);
    const [lessons, setLessons] = useState([]);
    const [loading, setLoading] = useState(true);

    const [room, setRoom] = useState("all");
    const [selectedStudent, setSelectedStudent] = useState("all");
    const [selectedLesson, setSelectedLesson] = useState("all");

    const [searchTerm, setSearchTerm] = useState("");
    const [sortOption, setSortOption] = useState("desc");

    const sortOptions = [
        { value: "desc", label: "‡∏•‡∏≥‡∏î‡∏±‡∏ö" },
        { value: "name", label: "‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡∏≤‡∏°‡∏ä‡∏∑‡πà‡∏≠ (‡∏Å-‡∏Æ)" },
        { value: "pre", label: "‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô Pre" },
        { value: "post", label: "‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô Post" },
    ];

    // ‡πÇ‡∏´‡∏•‡∏î‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
    useEffect(() => {
        getAllClassrooms()
            .then((res) => {
                if (res.status === "success") setClassrooms(res.data);
            })
            .catch((err) => console.error("Error fetching classrooms:", err));
    }, []);

    // ‡πÇ‡∏´‡∏•‡∏î‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
    useEffect(() => {
        getSubchapters()
            .then((res) => {
                if (res.status === "success") setLessons(res.data);
                window.allLessonsCount = res.data.length;
            })
            .catch((err) => console.error("Error fetching subchapters:", err));
    }, []);

    // ‡πÇ‡∏´‡∏•‡∏î summary
    useEffect(() => {
        setLoading(true);
        getStudentSummary(room)
            .then((res) => {
                if (res.status === "success") setStudents(res.data);
            })
            .catch((err) => console.error("Error fetching student summary:", err))
            .finally(() => setLoading(false));
    }, [room]);

    // Filter
    const filtered = useMemo(() => {
        let data = students;

        // ‡∏ü‡∏¥‡∏•‡πÄ‡∏ï‡∏≠‡∏£‡πå‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
        if (selectedStudent !== "all") {
            data = data.filter((s) => s.student_id === parseInt(selectedStudent));
        }

        // ‡∏ü‡∏¥‡∏•‡πÄ‡∏ï‡∏≠‡∏£‡πå‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
        if (selectedLesson !== "all") {
            const lessonId = parseInt(selectedLesson);
            data = data.filter((s) => {
                const subs = s.subchapters || {};
                return subs[lessonId] !== undefined;
            });
        }

        // ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤
        if (searchTerm.trim() !== "") {
            const q = searchTerm.toLowerCase();
            data = data.filter(
                (s) =>
                    s.name.toLowerCase().includes(q) ||
                    (s.classroom && s.classroom.toLowerCase().includes(q))
            );
        }

        // ‡πÄ‡∏£‡∏µ‡∏¢‡∏á
        data = [...data].sort((a, b) => {
            switch (sortOption) {
                case "desc":
                    return a.student_id - b.student_id;
                case "name":
                    return a.name.localeCompare(b.name, "th");
                case "pre":
                    return (b.pre_avg || 0) - (a.pre_avg || 0);
                case "post":
                    return (b.post_avg || 0) - (a.post_avg || 0);
                default:
                    return 0;
            }
        });

        return data;
    }, [students, searchTerm, sortOption, selectedStudent, selectedLesson]);

    if (loading) return <p>‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î...</p>;

    return (
        <section className="flex min-h-screen">
            <Sidebar />
            <div className="flex-1 p-8 space-y-8 mt-5 md:mt-0">
                {/* Toolbar ‡∏ü‡∏¥‡∏•‡πÄ‡∏ï‡∏≠‡∏£‡πå */}
                <div className="bg-white rounded-xl shadow p-4 flex flex-wrap items-center gap-4">
                    {/* ‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô */}
                    <div>
                        <label className="text-sm text-gray-600 mr-2">‡∏´‡πâ‡∏≠‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</label>
                        <select
                            value={room}
                            onChange={(e) => {
                                setRoom(e.target.value);
                                setSelectedStudent("all");
                                setSelectedLesson("all");
                            }}
                            className="border border-gray-200 rounded px-3 py-1 text-sm"
                        >
                            <option value="all">‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î</option>
                            {classrooms.map((c) => (
                                <option key={c.id} value={c.id}>
                                    {c.classroom_name}
                                </option>
                            ))}
                        </select>
                    </div>

                    {/* ‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô */}
                    <div>
                        <label className="text-sm text-gray-600 mr-2">‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</label>
                        <select
                            value={selectedStudent}
                            onChange={(e) => setSelectedStudent(e.target.value)}
                            className="border border-gray-200 rounded px-3 py-1 text-sm"
                        >
                            <option value="all">‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î</option>
                            {students.map((s) => (
                                <option key={s.student_id} value={s.student_id}>
                                    {s.name}
                                </option>
                            ))}
                        </select>
                    </div>

                    {/* ‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô */}
                    <div>
                        <label className="text-sm text-gray-600 mr-2">‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</label>
                        <select
                            value={selectedLesson}
                            onChange={(e) => setSelectedLesson(e.target.value)}
                            className="border border-gray-200 rounded px-3 py-1 text-sm"
                        >
                            <option value="all">‡∏ó‡∏∏‡∏Å‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</option>
                            {lessons.map((sc) => (
                                <option key={sc.subchapter_id} value={sc.subchapter_id}>
                                    {sc.subchapter_name}
                                </option>
                            ))}
                        </select>
                    </div>

                    {/* Search + Sort */}
                    <div className="ml-auto">
                        <SearchAndSort
                            searchTerm={searchTerm}
                            setSearchTerm={setSearchTerm}
                            sortValue={sortOption}
                            onSortChange={setSortOption}
                            sortOptions={sortOptions}
                        />
                    </div>
                </div>

                {/* Charts */}
                <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
                    <VideoChart students={filtered} selectedLesson={selectedLesson} />
                    {/* <PrePostChart students={filtered} /> */}
                    <QuizChart students={filtered} />
                </div>

                {/* ‚úÖ ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏™‡∏£‡∏∏‡∏õ */}
                <div className="bg-white rounded-xl shadow p-6">
                    <h3 className="font-semibold mb-4">‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</h3>
                    <table className="w-full text-sm">
                        <thead className="border-b border-gray-200">
                            <tr className="text-left">
                                <th className="py-2">‡∏ä‡∏∑‡πà‡∏≠</th>
                                <th className="py-2">‡∏´‡πâ‡∏≠‡∏á</th>
                                <th className="py-2 text-center">‡∏î‡∏π‡∏Ñ‡∏•‡∏¥‡∏õ‡∏ß‡∏¥‡∏î‡∏µ‡πÇ‡∏≠</th>
                                <th className="py-2 text-center">‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏™‡∏≠‡∏ö‡∏Å‡πà‡∏≠‡∏ô‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</th>
                                <th className="py-2 text-center">‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏™‡∏≠‡∏ö‡∏´‡∏•‡∏±‡∏á‡πÄ‡∏£‡∏µ‡∏¢‡∏ô</th>
                            </tr>
                        </thead>
                        <tbody>
                            {filtered.map((s) => (
                                <tr
                                    key={s.student_id}
                                    className="border-b border-gray-100 hover:bg-gray-50"
                                >
                                    <td className="py-2">{s.name}</td>
                                    <td className="py-2">{s.classroom}</td>
                                    <td className="py-2 text-center">
                                        {selectedLesson !== "all" ? (
                                            // üìå ‡∏Å‡∏£‡∏ì‡∏µ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ü‡∏¥‡∏•‡πÄ‡∏ï‡∏≠‡∏£‡πå‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
                                            <span className="px-2 py-1 text-xs bg-green-100 text-green-700 rounded">
                                                {s.subchapters?.[parseInt(selectedLesson)] === 1
                                                    ? "1/1 ‡∏Ñ‡∏•‡∏¥‡∏õ"
                                                    : "0/1 ‡∏Ñ‡∏•‡∏¥‡∏õ"}
                                            </span>
                                        ) : (
                                            // üìå ‡∏Å‡∏£‡∏ì‡∏µ‡∏£‡∏ß‡∏°‡∏ó‡∏∏‡∏Å‡∏ö‡∏ó‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
                                            <span className="px-2 py-1 text-xs bg-yellow-100 text-yellow-700 rounded">
                                                {s.video_completed}/{s.video_total} ‡∏Ñ‡∏•‡∏¥‡∏õ
                                            </span>
                                        )}
                                    </td>

                                    <td className="py-2 text-center">{s.pre_avg ?? "-"}</td>
                                    <td className="py-2 text-center">{s.post_avg ?? "-"}</td>
                                    {/* <td className="py-2 text-center">
                    {s.quiz_correct}/{s.quiz_total}
                  </td> */}
                                </tr>
                            ))}
                            {filtered.length === 0 && (
                                <tr>
                                    <td colSpan={6} className="text-center text-gray-400 py-4">
                                        ‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô
                                    </td>
                                </tr>
                            )}
                        </tbody>
                    </table>
                </div>
            </div>
        </section>
    );
}
